#include<iostream>
using namespace std;
int main()
{//  '<<' is called insertion operator. '>>' is called extraction operator.
    int num1,num2;
    cout<<"Enter the value of num1:\n"<<num1;
    cin>>num1;
    cout<<"Enter the value of num2:\n"<<num2;
    cin>>num2;
    cout<<"The sum is"<<num1+num2;
    return 0;
}